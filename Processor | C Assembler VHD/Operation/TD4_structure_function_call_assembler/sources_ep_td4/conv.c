#include <stdio.h>
/* Sprite 8x8 pour les numéros de 0 à 9
 * Un bit par pixel */
static char numbersprite[10][8]={
        {0x1C,0x22,0x22,0x22,0x22,0x22,0x22,0x1C}, /*0*/
        {0x8,0xC,0xA,0x8,0x8,0x8,0x8,0x8}, /*1*/
        {0x3C,0x22,0x40,0x20,0x10,0x8,0x4,0x7E}, /*2*/
        {0x3C,0x42,0x40,0x38,0x40,0x40, 0x42,0x3C}, /*3*/
        {0x10,0x18,0x14,0x12,0x7F,0x10,0x10,0x10}, /*4*/
        {0x7E,0x2,0x2,0x1E,0x60,0x40,0x60,0x1E}, /*5*/
        {0x70,0xC,0x6,0x3E,0x42,0x42,0x42,0x3C}, /*6*/
        {0x7E,0x40,0x20,0x10,0x10,0x8,0x4,0x2}, /*7*/
        {0x3C,0x42,0x42,0x3C,0x42,0x42,0x42,0x3C}, /*8*/
        {0x3C,0x66,0x42,0x66,0x7C,0x40,0x60,0x3E} /*9*/
};


int main(void)
{
		for (int i=0;i<10;i++) {
				printf("},\n{");		
			for (int j=0;j<8;j++){
				int b=numbersprite[i][j];
				int nb=0;
				for (int bit=0;bit<8;bit++) {
					nb=nb<<1;
					nb=nb|((b&1)==1?1:0);
					b=b>>1;
				}
				printf("0x%x,",nb);
			}
		}
        return 0;
}
